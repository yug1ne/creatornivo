function extractVars(prompt) {
  const set = new Set();
  const re = /\{\{([a-zA-Z0-9_]+)\}\}/g;
  let match;
  while ((match = re.exec(prompt))) set.add(match[1]);
  return [...set];
}

const prompt = fs.readFileSync(promptPath, "utf8");
const promptVars = extractVars(prompt);
const fieldKeys = variables.map((field) => field.key);
const missingFields = promptVars.filter((key) => !fieldKeys.includes(key));
const extraFields = fieldKeys.filter((key) => !promptVars.includes(key));

if (missingFields.length || extraFields.length) {
  console.error("Instagram Carousel prompt/form mismatch");
  console.error("Missing fields:", missingFields);
  console.error("Extra fields:", extraFields);
  process.exit(1);
}

const payload = {
  slug: "instagram-carousel",
  title: "Instagram Carousel",
  version: 1,
  generatedAt: new Date().toISOString(),
  fieldCount: variables.length,
  requiredKeys: variables
    .filter((field) => field.required)
    .map((field) => field.key),
  groups,
  variables,
};

fs.writeFileSync(outPath, JSON.stringify(payload, null, 2), "utf8");
console.log(`Wrote ${outPath} - ${variables.length} fields`);
